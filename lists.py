numbers = [1, 2, 3, 4]
print(numbers)

numbers[0]              # indexing
print(numbers[0])

numbers[1:3]            # slicing
print(numbers[1:3])

numbers.append(5)       # add
print(numbers)

numbers.remove(2)       # remove
print(numbers)

numbers.reverse()       # reverse  
print(numbers)

numbers.sort()          # ascending
print(numbers)

len(numbers)            # length
print(len(numbers))
